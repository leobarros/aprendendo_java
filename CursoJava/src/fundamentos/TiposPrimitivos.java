package fundamentos;

public class TiposPrimitivos {
	
	public static void main(String[] args) {
		//tipo primitivos booleano
		boolean bo1 = false;
		boolean bo2 = true;
		
		System.out.printf("%b %b \n", bo1, bo2);
		
		// tipo primitivo caracter
		char c1 = 'a';
		char c2 = '!';
		char c3 = '\u0050';
		
		System.out.printf("%c, %c, %c \n", c1, c2,c3);
		
	}

}
